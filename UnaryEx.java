class UnaryEx{
 public static void main(String args[]){
 int a=5;
 System.out.println(a);
 System.out.println(a++);
 System.out.println(a);
 System.out.println(++a);
 System.out.println(a);
 System.out.println(a--);
 System.out.println(a);
 System.out.println(--a);
 System.out.println(a);
 System.out.println(~a);
 System.out.println(!true);
 System.out.println(a--); 
 } 
}
/*Unary Operater
  1- Pre
  2- post
  3- negation
  4- inverting 
*/